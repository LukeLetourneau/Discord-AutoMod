fun fullTranslate(message: String) =
    convertLeet(
        convertUnicode(
            removeWhitespace(message)
        )
    )

fun convertLeet(message: String): String {
    return message
}

fun convertUnicode(message: String): String {
    return message
}

fun removeWhitespace(message: String): String {
    return message
}